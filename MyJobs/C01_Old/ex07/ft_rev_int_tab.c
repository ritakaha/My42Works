void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int aux[(size - 1)];

	i = 0;
	while (i < size)
	{
		aux[(size - 1) - i] = tab[i];
		i++;
	}
	j = 0;
	while (j < size)
	{
		tab[j] = aux[j];
		j++;
	}
}
