int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int value;

	i = 0;
	value = 0;
	while (value == 0 && (s1[i] != '\0' || s2[i] != '\0'))
	{
		value = s1[i] - s2[i];
		i++;
	}
	return (value);
}
